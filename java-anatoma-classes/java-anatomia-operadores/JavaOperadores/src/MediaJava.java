public class MediaJava {

    public static void main(String[] args) {
        double NotaUm = 8.0;
        double NotaDois = 6.0;
        double NotaTres = 7.0;
        double NotaQuatro = 9.0;

        double Media = (NotaUm + NotaDois + NotaTres + NotaQuatro) / 4;

        System.out.println("A média final do aluno será: " + Media);
    }
    
}
