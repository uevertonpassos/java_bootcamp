//Escreva um programa em Java que utilize um loop for para imprimir os números pares de 2 a 20.

public class Ex005 {
    public static void main(String[] args) {
       for(int x=1;x<=20;x++){

        if(x % 2==0) //% operador que representa módulo de uma divisão

            System.out.println(x);

    } 
    }
    

}
